"""
Unified serializers for the new chat schema.

One canonical `MessageSerializer` for all channel kinds (DM=1, GM=2, PM=3,
MDM=4). PM-specific fields (taskId, displayId, taskStatus, taskCommentCount)
ride in `metadata` JSON instead of top-level columns — the legacy
`pm_serializers` vs `dm_serializers` divergence (where PM had extra fields
and DM/GM/MDM returned slightly different timestamp shapes) does not exist
here.

DRF's default DateTimeField already emits ISO 8601 strings, so no explicit
DATETIME_FORMAT setting is required. The legacy code's `str(msg.ts_sent_at)`
calls were a regression that this rewrite eliminates by going back to the
DRF default.
"""

from rest_framework import serializers

from origin.models.chat.unified_models import (
    Channel,
    ChannelMember,
    Flag,
    Message,
    MessageAttachment,
    MessageMention,
    MessageReaction,
    Pin,
    ReadCursor,
)


class UserLiteSerializer(serializers.Serializer):
    """Compact user representation embedded in messages/reactions/etc.

    Mirrors the legacy wire shape so frontends rendering avatars don't
    need to re-fetch user info. Not a ModelSerializer because it draws
    from `CustomUser` but exposes a stable contract: just userId/name/
    email/avatar. Other fields (`tsLastSeen`, etc.) the legacy DM
    serializer returned as empty strings — keep them for compatibility
    while the FE finishes migrating.
    """

    userId = serializers.UUIDField(source="id", read_only=True)
    userName = serializers.CharField(source="username", read_only=True)
    userEmail = serializers.EmailField(source="email", read_only=True)
    avatarImgPath = serializers.CharField(source="profile_image_file_name", read_only=True)
    isSystemUser = serializers.BooleanField(source="is_system_user", read_only=True)


class MessageReactionSerializer(serializers.ModelSerializer):
    """One reaction row, embedded inside `MessageSerializer.reactions`.

    The legacy ReactionFact serializer keyed reactions by an integer
    `reaction_id` that auto-incremented per message. The new shape uses
    the row UUID directly — simpler and conflict-free.
    """

    id = serializers.UUIDField(read_only=True)
    user = UserLiteSerializer(read_only=True)
    emoji = serializers.CharField()
    tsSent = serializers.DateTimeField(source="ts_created_at", read_only=True)

    class Meta:
        model = MessageReaction
        fields = ["id", "user", "emoji", "tsSent"]


class MessageMentionSerializer(serializers.ModelSerializer):
    """One mention row.

    `viaGroup` is the UUID of a mention-group (`@engineering`, `@everyone`),
    null for direct `@user` mentions. The frontend uses this to render
    `@you via @engineering` in the inbox.
    """

    id = serializers.UUIDField(read_only=True)
    mentionedUserId = serializers.UUIDField(source="mentioned_user_id", read_only=True)
    viaGroupId = serializers.UUIDField(source="via_group_id", read_only=True, allow_null=True)
    tsCreated = serializers.DateTimeField(source="ts_created_at", read_only=True)

    class Meta:
        model = MessageMention
        fields = ["id", "mentionedUserId", "viaGroupId", "tsCreated"]


class MessageAttachmentSerializer(serializers.ModelSerializer):
    """One attachment row. The file URL is generated by DRF from the
    storage backend (S3 / disk / etc) at request time."""

    id = serializers.UUIDField(read_only=True)
    fileUrl = serializers.FileField(source="file", read_only=True)
    mime = serializers.CharField()
    sizeBytes = serializers.IntegerField(source="size_bytes")
    uploader = UserLiteSerializer(read_only=True)
    tsCreated = serializers.DateTimeField(source="ts_created_at", read_only=True)

    class Meta:
        model = MessageAttachment
        fields = ["id", "fileUrl", "mime", "sizeBytes", "uploader", "tsCreated"]


class MessageSerializer(serializers.ModelSerializer):
    """ONE serializer for every channel kind.

    Wire-shape contract (camelCase, ISO 8601 timestamps, UUIDs as strings):

        {
          "id": "<uuid>",                  // primary stable id
          "channelId": "<uuid>",
          "channelKind": 1|2|3|4,
          "seq": <int>,                    // legacy per-channel message_id
          "sender": <UserLite>,
          "body": <JSON>,                  // blocknote-style block array
          "bodyText": <str>,               // first-line preview
          "parentId": "<uuid>" | null,
          "threadRootId": "<uuid>" | null,
          "isThreadReply": <bool>,
          "replyCount": <int>,
          "reactions": [<MessageReaction>, ...],
          "mentions": [<MessageMention>, ...],
          "attachments": [<MessageAttachment>, ...],
          "metadata": { ... },             // PM: {taskId, displayId, taskStatus, taskCommentCount}
          "editedAt": "<iso>" | null,
          "deletedAt": "<iso>" | null,
          "tsSent": "<iso>",
          "tsUpdated": "<iso>",
        }

    PM-specific fields are in `metadata`. No per-kind divergence anywhere.
    """

    id = serializers.UUIDField(read_only=True)
    channelId = serializers.UUIDField(source="channel_id", read_only=True)
    # SerializerMethodField rather than `source="channel.kind"` to avoid a
    # gratuitous join when the channel is already loaded via select_related.
    channelKind = serializers.SerializerMethodField()
    sender = UserLiteSerializer(read_only=True)
    seq = serializers.IntegerField(read_only=True)
    body = serializers.JSONField()
    bodyText = serializers.CharField(source="body_text")
    parentId = serializers.UUIDField(source="parent_id", read_only=True, allow_null=True)
    threadRootId = serializers.UUIDField(source="thread_root_id", read_only=True, allow_null=True)
    isThreadReply = serializers.BooleanField(source="is_thread_reply", read_only=True)
    replyCount = serializers.IntegerField(source="reply_count", read_only=True)
    reactions = MessageReactionSerializer(many=True, read_only=True)
    mentions = MessageMentionSerializer(many=True, read_only=True)
    attachments = MessageAttachmentSerializer(many=True, read_only=True)
    metadata = serializers.JSONField()
    editedAt = serializers.DateTimeField(source="edited_at", read_only=True, allow_null=True)
    deletedAt = serializers.DateTimeField(source="deleted_at", read_only=True, allow_null=True)
    tsSent = serializers.DateTimeField(source="ts_sent_at", read_only=True)
    tsUpdated = serializers.DateTimeField(source="ts_updated_at", read_only=True)

    class Meta:
        model = Message
        fields = [
            "id",
            "channelId",
            "channelKind",
            "sender",
            "seq",
            "body",
            "bodyText",
            "parentId",
            "threadRootId",
            "isThreadReply",
            "replyCount",
            "reactions",
            "mentions",
            "attachments",
            "metadata",
            "editedAt",
            "deletedAt",
            "tsSent",
            "tsUpdated",
        ]

    def get_channelKind(self, obj):
        # `obj.channel` may be a deferred FK; channel_id_kind cache would
        # be ideal, but for now we accept the second column read. Views
        # using select_related("channel") avoid the extra query.
        return obj.channel.kind if obj.channel else None


