"""
Unified serializers for the new chat schema.

One canonical `MessageSerializer` for all channel kinds (DM=1, GM=2, PM=3,
MDM=4). PM-specific fields (taskId, displayId, taskStatus, taskCommentCount)
are top-level, derived off the `task` FK — the legacy `pm_serializers` vs
`dm_serializers` divergence (where PM had extra fields and DM/GM/MDM returned
slightly different timestamp shapes) does not exist here.

DRF's default DateTimeField already emits ISO 8601 strings, so no explicit
DATETIME_FORMAT setting is required. The legacy code's `str(msg.ts_sent_at)`
calls were a regression that this rewrite eliminates by going back to the
DRF default.
"""

from django.db.models import Count, IntegerField, OuterRef, Subquery
from django.db.models.functions import Coalesce
from rest_framework import serializers

from origin.models.chat.unified_models import (
    Activity,
    Channel,
    ChannelKind,
    ChannelMember,
    Flag,
    Message,
    MessageAttachment,
    MessageMention,
    MessageReaction,
    Pin,
    ReadCursor,
)


class UserLiteSerializer(serializers.Serializer):
    """Compact user representation embedded in messages/reactions/etc.

    Mirrors the legacy wire shape so frontends rendering avatars don't
    need to re-fetch user info. Not a ModelSerializer because it draws
    from `CustomUser` but exposes a stable contract: just userId/name/
    email/avatar. Other fields (`tsLastSeen`, etc.) the legacy DM
    serializer returned as empty strings — keep them for compatibility
    while the FE finishes migrating.
    """

    userId = serializers.UUIDField(source="id", read_only=True)
    userName = serializers.CharField(source="username", read_only=True)
    userEmail = serializers.EmailField(source="email", read_only=True)
    avatarImgPath = serializers.CharField(source="profile_image_file_name", read_only=True)
    isSystemUser = serializers.BooleanField(source="is_system_user", read_only=True)


class MessageReactionSerializer(serializers.ModelSerializer):
    """One reaction row, embedded inside `MessageSerializer.reactions`.

    The legacy ReactionFact serializer keyed reactions by an integer
    `reaction_id` that auto-incremented per message. The new shape uses
    the row UUID directly — simpler and conflict-free.
    """

    id = serializers.UUIDField(read_only=True)
    user = UserLiteSerializer(read_only=True)
    emoji = serializers.CharField()
    tsSent = serializers.DateTimeField(source="ts_created_at", read_only=True)

    class Meta:
        model = MessageReaction
        fields = ["id", "user", "emoji", "tsSent"]


class MessageMentionSerializer(serializers.ModelSerializer):
    """One mention row.

    `viaGroup` is the UUID of a mention-group (`@engineering`, `@everyone`),
    null for direct `@user` mentions. The frontend uses this to render
    `@you via @engineering` in the inbox.
    """

    id = serializers.UUIDField(read_only=True)
    mentionedUserId = serializers.UUIDField(source="mentioned_user_id", read_only=True)
    viaGroupId = serializers.UUIDField(source="via_group_id", read_only=True, allow_null=True)
    tsCreated = serializers.DateTimeField(source="ts_created_at", read_only=True)

    class Meta:
        model = MessageMention
        fields = ["id", "mentionedUserId", "viaGroupId", "tsCreated"]


