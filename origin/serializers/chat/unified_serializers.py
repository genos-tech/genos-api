class ChannelSerializer(serializers.ModelSerializer):
    """One channel row. The chat-list endpoint returns a list of these.

    `latestMessage` is denormalized at serialize-time (via the view's
    annotated queryset) so the chat-list rows render without per-row
    follow-up queries. `unreadCount` derives from the user's ReadCursor;
    see ChannelListView for the prefetch wiring.
    """

    id = serializers.UUIDField(read_only=True)
    kind = serializers.IntegerField(read_only=True)
    title = serializers.CharField()
    profileImageUrl = serializers.CharField(source="profile_image_url")
    projectId = serializers.IntegerField(source="project_id", read_only=True, allow_null=True)
    ownerId = serializers.UUIDField(source="owner_id", read_only=True, allow_null=True)
    isPrivate = serializers.BooleanField(source="is_private")
    tsCreated = serializers.DateTimeField(source="ts_created_at", read_only=True)
    tsUpdated = serializers.DateTimeField(source="ts_updated_at", read_only=True)

    # Populated by the view's annotated queryset; see ChannelListView.
    latestMessage = serializers.SerializerMethodField()
    unreadCount = serializers.SerializerMethodField()

    class Meta:
        model = Channel
        fields = [
            "id",
            "kind",
            "title",
            "profileImageUrl",
            "projectId",
            "ownerId",
            "isPrivate",
            "latestMessage",
            "unreadCount",
            "tsCreated",
            "tsUpdated",
        ]

    def get_latestMessage(self, obj):
        msg = getattr(obj, "_latest_message", None)
        if msg is None:
            return None
        return MessageSerializer(msg, context=self.context).data

    def get_unreadCount(self, obj):
        # Annotated by the view as the number of messages whose `seq` is
        # greater than the user's last_read seq. Falls back to 0 if the
        # view didn't annotate (e.g. anonymous / non-member access).
        return getattr(obj, "_unread_count", 0)


