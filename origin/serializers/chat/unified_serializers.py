class ActivitySerializer(serializers.ModelSerializer):
    """One activity-feed entry. Surfaces the recipient's view of an
    event triggered by `actor` (someone reacted / replied / @mentioned).

    Wire fields stay camelCase to match the message/reaction shape the
    FE already consumes. `message` and `actor` are embedded so the
    sidebar can render the row without a follow-up fetch.
    """

    id = serializers.UUIDField(read_only=True)
    activityType = serializers.IntegerField(source="activity_type", read_only=True)
    recipientUserId = serializers.UUIDField(source="recipient_id", read_only=True)
    channelId = serializers.UUIDField(source="channel_id", read_only=True)
    channelKind = serializers.SerializerMethodField()
    messageId = serializers.UUIDField(source="message_id", read_only=True)
    # Null for channel-backed activities; the legacy chat_type namespace
    # (5=task body, 6/7/8=notes) for channel-less surface mentions. The
    # FE derives the row's chat_type from `surfaceType ?? channelKind`,
    # and reads the routing ids (taskId / projectId / noteId / chatId)
    # from `meta`.
    surfaceType = serializers.IntegerField(source="surface_type", read_only=True, allow_null=True)
    actor = UserLiteSerializer(read_only=True, allow_null=True)
    # `allow_null`: surface activities have no backing Message.
    message = MessageSerializer(read_only=True, allow_null=True)
    isRead = serializers.BooleanField(source="is_read", read_only=True)
    tsCreated = serializers.DateTimeField(source="ts_created_at", read_only=True)

    class Meta:
        model = Activity
        fields = [
            "id",
            "activityType",
            "recipientUserId",
            "channelId",
            "channelKind",
            "messageId",
            "surfaceType",
            "actor",
            "message",
            "meta",
            "isRead",
            "tsCreated",
        ]

    def get_channelKind(self, obj):
        return obj.channel.kind if obj.channel else None


class DeltaEnvelopeSerializer(serializers.Serializer):
    """Wraps a delta-sync response.

    The frontend persists `serverTime` as the `since` for the next sync
    request. `forceFullReload=True` tells the client to clear its IDB
    for this channel before applying `data`. `data` shape is endpoint-
    specific — `MessagesDeltaView` returns
        {messages: [...], deletes: [...], reactions_changed: [...]}
    while `ChannelListView` returns
        {channels: [...]}
    """

    serverTime = serializers.DateTimeField()
    forceFullReload = serializers.BooleanField(default=False)
    data = serializers.JSONField()
